const cardNameInput = cardModal.querySelector("#add-card-name-input");
const cardLinkInput = cardModal.querySelector("#add-card-link-input");

const previewModal = document.querySelector("#preview-modal");
const previewModalImageEl = previewModal.querySelector(".modal__image");
const previewModalCaptionEl = previewModal.querySelector(".modal__caption");
const previewModalCloseBtn = previewModal.querySelector(
  ".modal__close-btn_type_preview"
);
const modals = document.querySelectorAll(".modal");

// Card related elements
const cardTemplate = document.querySelector("#card-template");
const cardsList = document.querySelector(".cards__list");

function getCardElement(data) {
  const cardElement = cardTemplate.content
    .querySelector(".card")
    .cloneNode(true);
  const cardNameEl = cardElement.querySelector(".card__title");
  const cardImageEl = cardElement.querySelector(".card__image");
  const cardLikeBtn = cardElement.querySelector(".card__like-button");
  const deleteButton = cardElement.querySelector(".card__delete-button");

  cardNameEl.textContent = data.name;
  cardImageEl.src = data.link;
  cardImageEl.alt = data.name;

  cardLikeBtn.addEventListener("click", () => {
    cardLikeBtn.classList.toggle("card__like-button_liked");
  });

  deleteButton.addEventListener("click", () => {
    const cardToDelete = deleteButton.closest(".card");
    cardToDelete.remove();
  });

  cardImageEl.addEventListener("click", () => {
    openModal(previewModal);
    previewModalCaptionEl.textContent = data.name;
    previewModalImageEl.src = data.link;
    previewModalImageEl.alt = data.name;
  });

  return cardElement;
}

// This function is opens the modal
function openModal(modal) {
  // Setting the name input value to be our profile name textcontent

  // adding the modal_opened class which has the visible css property
  modal.classList.add("modal_opened");
  document.addEventListener("keydown", handleEscClose);
}

// This function is closing the modal by removing the modal_opened class which in the css has visible
function closeModal(modal) {
  modal.classList.remove("modal_opened");
  document.removeEventListener("keydown", handleEscClose);
}

function closeModalOnOverlay(evt) {
  if (evt.target === evt.currentTarget) {
    closeModal(evt.target);
  }
}

function handleEscClose(evt) {
  if (evt.key === "Escape") {
    const openedModal = document.querySelector(".modal_opened");
    if (openedModal) {
      closeModal(openedModal);
    }
  }
}

function handleEditFormSubmit(evt) {
  evt.preventDefault();
  profileName.textContent = editModalNameInput.value;
  profileDescription.textContent = editModalDescriptionInput.value;
  closeModal(editModal);
}

function handleAddCardSubmit(evt) {
  evt.preventDefault();

  const nameIsValid = checkInputValidity(cardForm, cardNameInput, settings);
  const linkIsValid = checkInputValidity(cardForm, cardLinkInput, settings);

  if (nameIsValid && linkIsValid) {
    // 1. Create a new card object using the input values
    const inputValues = {
      name: cardNameInput.value,
      link: cardLinkInput.value,
    };

    // 2. Create a new card element
    const cardElement = getCardElement(inputValues);

    // 3. Add the new card to the beginning of the cards list
    cardsList.prepend(cardElement);
    disableButton(cardSubmitBtn, settings);

    // 4. Close the modal
    closeModal(cardModal);

    // 5. Reset the form
    cardForm.reset();
  }
}

